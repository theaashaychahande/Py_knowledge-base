"""
1. Definition:  
Operators are symbols that do math or compare things, like +, -, >, etc  
2. Why Use:  
To do calculations, check conditions, or combine values quickly
"""

# 3. Format Examples:
# Math:       x + y, x - y, x * y, x / y
# Comparison: x > y, x == y, x != y
# Logic:      and, or, not

# 4. Code Examples:
# Math
print(10 + 3)   # 13
print(10 // 3)  # 3 (integer division)
print(10 % 3)   # 1 (remainder)

# Comparison
age = 19
print(age >= 18)  # True (checks if 19 is >= 18)

# Logic
has_money = True
has_time = False
print(has_money and has_time)  # False (both need to be True)
print(has_money or has_time)   # True (only one needs to be True)

# 5. Output:
# 13
# 3
# 1
# True
# False
# True
